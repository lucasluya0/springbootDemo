package com.lucas.SystemSpringBoot.entity;

import java.io.Serializable;

public class ResourcesDetail implements Serializable {
    private String resourcesId;//资源Id
    private String resourcesName;//资源名称
    private String resourcesUrl;//资源Url
    private String type;//资源类型
    private String parentId;//资源的父类Id

    public String getResourcesId() {
        return resourcesId;
    }

    public void setResourcesId(String resourcesId) {
        this.resourcesId = resourcesId;
    }

    public String getResourcesName() {
        return resourcesName;
    }

    public void setResourcesName(String resourcesName) {
        this.resourcesName = resourcesName;
    }

    public String getResourcesUrl() {
        return resourcesUrl;
    }

    public void setResourcesUrl(String resourcesUrl) {
        this.resourcesUrl = resourcesUrl;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getParentId() {
        return parentId;
    }

    public void setParentId(String parentId) {
        this.parentId = parentId;
    }
}
